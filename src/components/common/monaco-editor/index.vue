<template>
  <div id="monaco-editor" class="w-full h-full">
  </div>
  <div class="absolute bottom-0 left-0 text-sm text-red-600">{{ errorTip }}</div>
</template>

<script>
import * as monaco from 'monaco-editor'
import { onBeforeUnmount, onMounted, ref } from 'vue'
import { isPlainObject } from 'lodash'

export default {
  name: 'monaco-editor',
  props: {
    code: {
      type: String,
      required: true
    }
  },
  emits: ['change'],
  setup (props, { emit }) {
    let editorInstance
    const errorTip = ref('')
    onMounted(() => {
      console.log('editorInstance')
      editorInstance = monaco.editor.create(document.querySelector('#monaco-editor'), {
        value: '',
        language: 'json',
        theme: 'vs-dark',
        formatOnPaste: true,
        fontSize: 18,
        automaticLayout: true
      })
      editorInstance.setValue(props.code)
      editorInstance.onDidChangeModelContent(() => {
        try {
          const result = JSON.parse(editorInstance.getValue())
          if (!isPlainObject(result)) {
            errorTip.value = '数据源不是Object对象'
            return
          } else {
            errorTip.value = ''
            emit('change', result)
          }
        } catch (e) {
          errorTip.value = '格式不正确'
        }
      })
    })
    onBeforeUnmount(() => {
      editorInstance.dispose()
    })
    return {
      errorTip
    }
  }
}
</script>

